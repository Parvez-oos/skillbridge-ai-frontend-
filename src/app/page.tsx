function BootSequence({ onComplete }: { onComplete: () => void }) {
  const [currentLine, setCurrentLine] = useState(0);
  const [progress, setProgress] = useState(0);
  const [isDone, setIsDone] = useState(false);

  useEffect(() => {
    if (currentLine < bootSequence.length) {
      const timer = setTimeout(() => setCurrentLine((p) => p + 1), 600);
      return () => clearTimeout(timer);
    } else {
      const timer = setTimeout(() => {
        setIsDone(true);
        setTimeout(onComplete, 400);
      }, 300);
      return () => clearTimeout(timer);
    }
  }, [currentLine, onComplete]);

  useEffect(() => {
    const interval = setInterval(() => {
      setProgress((p) => Math.min(100, p + Math.random() * 8 + 2));
    }, 50);
    return () => clearInterval(interval);
  }, []);

  return (
    <motion.div
      className="fixed inset-0 z-[100] flex items-center justify-center bg-background"
      animate={isDone ? { opacity: 0 } : { opacity: 1 }}
      transition={{ duration: 0.4 }}
    >
      <div className="w-full max-w-md px-6">
        <div className="mb-8 text-center">
          <div className="w-16 h-16 rounded-2xl bg-gradient-to-br from-primary to-accent flex items-center justify-center mx-auto mb-4 shadow-lg shadow-primary/20">
            <Sparkles className="w-8 h-8 text-white" />
          </div>
          <h1 className="text-2xl font-bold gradient-text">SkillBridge AI</h1>
        </div>

        <div className="space-y-3 mb-8">
          {bootSequence.map((line, i) => (
            <motion.div
              key={i}
              initial={{ opacity: 0, x: -10 }}
              animate={i < currentLine ? { opacity: 1, x: 0 } : i === currentLine ? { opacity: 1, x: 0 } : {}}
              transition={{ duration: 0.3 }}
              className={`flex items-center gap-3 text-sm ${i < currentLine ? 'text-foreground' : i === currentLine ? 'text-primary' : 'text-muted-foreground/40'}`}
            >
              {i < currentLine ? (
                <Check className="w-4 h-4 text-success shrink-0" />
              ) : i === currentLine ? (
                <motion.div animate={{ rotate: 360 }} transition={{ duration: 1, repeat: Infinity, ease: 'linear' }}>
                  <line.icon className="w-4 h-4 text-primary shrink-0" />
                </motion.div>
              ) : (
                <line.icon className="w-4 h-4 shrink-0" />
              )}
              <span className="font-mono">{line.text}</span>
            </motion.div>
          ))}
        </div>

        <div className="space-y-2">
          <div className="h-1.5 bg-muted/50 rounded-full overflow-hidden">
            <motion.div
              className="h-full bg-gradient-to-r from-primary to-accent rounded-full"
              initial={{ width: 0 }}
              animate={{ width: `${progress}%` }}
              transition={{ duration: 0.1 }}
            />
          </div>
          <p className="text-xs text-muted-foreground text-center font-mono">{Math.round(progress)}% complete</p>
        </div>
      </div>
    </motion.div>
  );
}

